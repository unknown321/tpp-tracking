afgh_gimmick.Messages = function()
	return Tpp.StrCode32Table({
		GameObject = {
			{
				msg = "BreakGimmick",
				func = TppGimmick.BreakGimmick,
				option = { isExecMissionPrepare = true },
			},
			{
				msg = "SwitchGimmick",
				func = TppGimmick.SwitchGimmick,
				option = { isExecMissionPrepare = true },
			},
			nil,
		},
	})
end

function afgh_gimmick.OnInitialize()
	TppGimmick.SetUpIdentifierTable(afgh_gimmick.gimmickIdentifierParamTable)
	TppGimmick.SetUpBreakConnectTable(afgh_gimmick.gimmickBreakConnectTable)
	TppGimmick.SetUpCheckBrokenAndBreakConnectTable(afgh_gimmick.checkBrokenAndBreakConnectTable)
	TppGimmick.SetUpConnectVisibilityTable(afgh_gimmick.gimmickVisibilityiConnectTable)
	TppGimmick.SetUpConnectPowerCutTable(afgh_gimmick.gimmickPowerCutConnectTable)
	afgh_gimmick.messageExecTable = Tpp.MakeMessageExecTable(afgh_gimmick.Messages())
	Gimmick.RegistGimmickNameIdentifier(afgh_gimmick.gimmickIdentifierParamTable)
end

function afgh_gimmick.OnMessage(sender, messageId, arg0, arg1, arg2, arg3, strLogText)
	Tpp.DoMessage(
		afgh_gimmick.messageExecTable,
		TppMission.CheckMessageOption,
		sender,
		messageId,
		arg0,
		arg1,
		arg2,
		arg3,
		strLogText
	)
end

return afgh_gimmick
